const Hero = require('./Hero');

class Boomerang {
  constructor() {
    this.skin = 'üçª';
    this.position = 1;
    this.flag = true;
  }

  fly() {
    this.flag = true;
    const timer = setInterval(() => {
      this.moveRight();
      if (this.flag === false) {
        clearInterval(timer);
      }
    }, 100);
  }

  flyBack(position) {
    const anyTimer = setInterval(() => {
      this.moveLeft();
      if (this.position == position + 1) {
        clearInterval(anyTimer);
      }
    }, 100);
  }

  moveLeft() {
    // –ò–¥—ë–º –≤–ª–µ–≤–æ.
    this.position -= 1;
  }

  moveRight() {
    // –ò–¥—ë–º –≤–ø—Ä–∞–≤–æ.
    this.position += 1;
  }
}
